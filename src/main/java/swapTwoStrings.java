public class swapTwoStrings {
    public static void main(String[] args) {
        String a="Pawan";
        String b="Joshi";
        String c ="";
        System.out.println("Before Swapping a= "+a+" and b= "+b);

        c=a;
        a=b;
        b=c;

        System.out.println("After Swapping a= "+a+" and b= "+b);

    }
}
